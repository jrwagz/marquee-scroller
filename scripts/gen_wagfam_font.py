#!/usr/bin/env python3
"""Generate marquee/WagfamFont.h from human-readable ASCII-art glyphs.

The output is an Adafruit_GFX-compatible header containing six GFXfont
structs:

  * ``WagfamBlock``  — chunky 5x7 ALL-CAPS (issue #106, the original)
  * ``WagfamTall``   — clean 5x8 full-height with distinct lowercase
  * ``WagfamBold``   — 5x8 thick (procedurally derived from WagfamTall by
    1-pixel stroke thickening). Same shapes as Tall, beefier presence
  * ``WagfamSlim``   — 3x8 ultra-narrow with distinct lowercase
  * ``WagfamOutline``— 5x8 hollow (procedurally derived: shows the 4-
    connected boundary of WagfamBold's strokes)
  * ``WagfamDigi``   — 5x8 seven-segment / digital-clock style with
    distinct lowercase

All glyphs cover printable ASCII 0x20-0x7E. Each glyph is described as a
``height``-row x ``width``-column grid of '#' (on) and '.' (off), packed
MSB-first and emitted as PROGMEM bytes alongside a per-glyph metadata
table.

Adafruit_GFX rendering metrics:
  * 7-tall fonts: yAdvance=8, yOffset=-7, xAdvance=width+1.
    Caller positions cursor at (x, 7); pixel rows 0..6 of the matrix
    receive the glyph.
  * 8-tall fonts: yAdvance=8 (still — the 32x8 matrix has no inter-line
    leading), yOffset=-8, xAdvance=width+1. Caller positions cursor at
    (x, 8); pixel rows 0..7 of the matrix receive the glyph (every pixel
    used).

Run:  ./scripts/gen_wagfam_font.py
"""
from __future__ import annotations

from collections.abc import Callable
from pathlib import Path

# ---------------------------------------------------------------------------
# Glyph data structures
# ---------------------------------------------------------------------------
# Each font is described by:
#   name         — C identifier (e.g. "WagfamTall"; the GFXfont struct name)
#   width        — glyph cell width in pixels
#   height       — glyph cell height in pixels
#   x_advance    — cursor advance per glyph (typically width + 1 for spacing)
#   glyphs       — dict from char to ASCII art (height rows of width chars)
#   lowercase_strategy — one of:
#         "fallback"  — lowercase a-z renders the uppercase glyph
#                       (matches WagfamBlock's original behavior)
#         "distinct"  — lowercase has its own glyph art in the dict
# Unknown glyphs render as a solid block so missing entries are obvious.
# ---------------------------------------------------------------------------


def parse_glyph(art: str, width: int = 5, height: int = 7) -> list[int]:
    """Parse a height-line glyph art string into ``height`` row-bytes.

    Each row's ``width`` pixels are packed into the high ``width`` bits of a
    single byte; Adafruit_GFX's row reader walks bits left-to-right (MSB
    first) within each row. Rows shorter than ``width`` are right-padded
    with OFF; rows longer are truncated.
    """
    rows = [r for r in art.splitlines() if r != ""]
    if len(rows) != height:
        raise ValueError(
            f"Glyph must have exactly {height} rows, got {len(rows)}: {rows!r}"
        )
    out: list[int] = []
    for row in rows:
        row = row.ljust(width, ".")[:width]
        bits = 0
        for i, ch in enumerate(row):
            if ch == "#":
                bits |= 1 << (7 - i)  # MSB-first into a byte
            elif ch != ".":
                raise ValueError(f"Glyph row has invalid char {ch!r}: {row!r}")
        out.append(bits)
    return out


def pack_bitmap(rows: list[int], width: int, height: int) -> list[int]:
    """Pack ``height`` rows of ``width`` pixels into a continuous bitstream.

    Each row begins flush against the previous one (no per-row byte padding,
    matching Adafruit_GFX). Trailing bits in the final byte are zero.
    """
    bits: list[int] = []
    for r in range(height):
        row = rows[r]
        for i in range(width):
            bits.append(1 if (row >> (7 - i)) & 1 else 0)
    out: list[int] = []
    for i in range(0, len(bits), 8):
        chunk = bits[i : i + 8]
        b = 0
        for j, bit in enumerate(chunk):
            b |= bit << (7 - j)
        out.append(b)
    return out


