def _resolve_derived(fonts: list[FontDef]) -> None:
    """Materialize derive_from entries by transforming the source font's art."""
    by_name = {f.name: f for f in fonts}
    for font in fonts:
        if font.derive_from is None:
            continue
        src_name, transform = font.derive_from
        src = by_name[src_name]
        font.glyphs = {
            ch: transform(art, src.width, src.height)
            for ch, art in src.glyphs.items()
        }


def _glyph_for(font: FontDef, code: int) -> str:
    """Return the ASCII art for codepoint ``code`` from ``font``, applying
    the lowercase fallback if appropriate. Missing entries become a solid
    block so omissions are visually obvious."""
    ch = chr(code)
    if "a" <= ch <= "z" and font.lowercase_strategy == "fallback":
        src = ch.upper()
    else:
        src = ch
    if src in font.glyphs:
        return font.glyphs[src]
    return "\n".join(["#" * font.width] * font.height)


def _emit_font(font: FontDef, lines: list[str]) -> tuple[int, int]:
    """Emit PROGMEM tables + GFXfont struct for ``font``. Returns
    (bitmap_byte_count, glyph_count) for the caller to sanity-check."""
    first = 0x20
    last = 0x7E
    y_offset = -font.height

    glyph_entries: list[tuple[int, int, int, int, int, int, str]] = []
    bitmap_bytes: list[int] = []
    bitmap_offset = 0
    bytes_per_glyph = (font.width * font.height + 7) // 8

    for code in range(first, last + 1):
        art = _glyph_for(font, code)
        rows = parse_glyph(art, font.width, font.height)
        packed = pack_bitmap(rows, font.width, font.height)
        bitmap_bytes.extend(packed)
        glyph_entries.append(
            (bitmap_offset, font.width, font.height,
             font.x_advance, 0, y_offset, chr(code))
        )
        bitmap_offset += len(packed)

    bm_name = f"{font.name}Bitmaps"
    gl_name = f"{font.name}Glyphs"

    lines.append(f"const uint8_t {bm_name}[] PROGMEM = {{")
    for i in range(0, len(bitmap_bytes), 12):
        chunk = bitmap_bytes[i : i + 12]
        lines.append("  " + ", ".join(f"0x{b:02X}" for b in chunk) + ",")
    lines.append("};")
    lines.append("")
    lines.append(f"const GFXglyph {gl_name}[] PROGMEM = {{")
    for off, w, h, adv, xo, yo, ch in glyph_entries:
        comment = f"// 0x{ord(ch):02X} {ch!r}"
        lines.append(
            f"  {{ {off}, {w}, {h}, {adv}, {xo}, {yo} }},  {comment}"
        )
    lines.append("};")
    lines.append("")
    lines.append(f"const GFXfont {font.name} PROGMEM = {{")
    lines.append(f"  (uint8_t *){bm_name},")
    lines.append(f"  (GFXglyph *){gl_name},")
    lines.append(f"  0x{first:02X}, 0x{last:02X},  // first, last")
    lines.append(f"  {font.height + 1}                  // yAdvance")
    lines.append("};")
    lines.append("")

    expected = len(glyph_entries) * bytes_per_glyph
    if expected != len(bitmap_bytes):
        raise RuntimeError(
            f"{font.name}: bitmap size {len(bitmap_bytes)} != expected {expected}"
        )
    return len(bitmap_bytes), len(glyph_entries)


