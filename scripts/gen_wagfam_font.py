# ---------------------------------------------------------------------------
# Procedural transforms — derive new fonts from WagfamTall by applying a
# uniform style modifier. Each one preserves Tall's distinct-lowercase
# property (since the transform runs over Tall's entire dict, including the
# lowercase entries). Used for Italic, Pixel, Inverse, Stencil so we don't
# have to hand-design hundreds of glyphs at 5x8 where the visual differences
# come from a consistent style rule rather than per-letter polish.
# ---------------------------------------------------------------------------


def _grid(art: str, w: int, h: int) -> list[list[str]]:
    rows = [r.ljust(w, ".")[:w] for r in art.splitlines() if r != ""]
    return [list(r) for r in rows]


def _to_art(grid: list[list[str]]) -> str:
    return "\n".join("".join(r) for r in grid)


def italic_glyph(art: str, w: int, h: int) -> str:
    """Slant the top half of the glyph rightward by 1 column.

    At 5x8 a true geometric slant overflows; this approximation pushes rows
    0..3 right by 1 (clamping the rightmost column off-cell) and leaves rows
    4..7 in place, giving a visible "lean" between the upper and lower halves
    that reads as italics on a scrolling matrix.
    """
    g = _grid(art, w, h)
    out = [["."] * w for _ in range(h)]
    for y in range(h):
        shift = 1 if y < h // 2 else 0
        for x in range(w):
            if g[y][x] != "#":
                continue
            nx = x + shift
            if 0 <= nx < w:
                out[y][nx] = "#"
    return _to_art(out)


def serif_glyph(art: str, w: int, h: int) -> str:
    """Flare the top and bottom rows outward into serif caps.

    For every ON pixel in the *first* row of the glyph (or the *last* drawn
    row, defined as h-2 since row h-1 is the inter-glyph gap), spread one
    pixel to the left and one to the right wherever those slots are
    currently OFF. The result: vertical stems sprout ##.## bases and tops
    (a classic slab-serif), horizontal bars get pulled out to the cell
    edges, and crossbars in the middle of the glyph are left untouched.
    """
    g = _grid(art, w, h)
    out = [row[:] for row in g]
    bottom = max(0, h - 2)  # last drawn row (skip the inter-glyph gap)
    for serif_row in (0, bottom):
        if serif_row >= h:
            continue
        for x in range(w):
            if g[serif_row][x] != "#":
                continue
            if x - 1 >= 0 and out[serif_row][x - 1] == ".":
                out[serif_row][x - 1] = "#"
            if x + 1 < w and out[serif_row][x + 1] == ".":
                out[serif_row][x + 1] = "#"
    return _to_art(out)


def pixel_glyph(art: str, w: int, h: int) -> str:
    """Apply a deterministic halftone speckle to ON pixels.

    Turns off about one in five ON pixels using a simple `(x*7 + y*13) % 5
    == 0` mask — chosen to (a) be visually irregular (no obvious checker
    pattern), (b) leave the silhouette dense enough that letters still read,
    and (c) stay deterministic so the bitmap is reproducible across runs.
    Letterforms inherit Tall's distinct lowercase since we transform every
    glyph in TALL_GLYPHS, including a-z.
    """
    g = _grid(art, w, h)
    out = [row[:] for row in g]
    for y in range(h):
        for x in range(w):
            if g[y][x] == "#" and (x * 7 + y * 13) % 5 == 0:
                out[y][x] = "."
    return _to_art(out)


def inverse_glyph(art: str, w: int, h: int) -> str:
    """Render the letter as negative space inside a solid block.

    The top h-1 rows are flipped (every ON becomes OFF and vice versa) so
    the original silhouette appears as a hole in a filled cell. The last row
    is forced OFF to act as a between-glyph gap on the matrix; without it,
    adjacent inverse glyphs would visually merge into one continuous bar.
    """
    g = _grid(art, w, h)
    out = [["."] * w for _ in range(h)]
    for y in range(h - 1):
        for x in range(w):
            out[y][x] = "." if g[y][x] == "#" else "#"
    return _to_art(out)


def stencil_glyph(art: str, w: int, h: int) -> str:
    """Cut a 1-pixel gap in the middle of every long vertical stroke.

    Real-world stencils need bridges to keep counter-shapes (the holes in O,
    P, A, etc.) from falling out. Mimicking that: for each column with a
    contiguous ON run of length ≥3, the middle pixel is turned off. Looks
    like sprayed-through-cardboard letters.
    """
    g = _grid(art, w, h)
    out = [row[:] for row in g]
    for x in range(w):
        y = 0
        while y < h:
            if g[y][x] != "#":
                y += 1
                continue
            start = y
            while y < h and g[y][x] == "#":
                y += 1
            end = y - 1
            if end - start + 1 >= 3:
                mid = (start + end) // 2
                out[mid][x] = "."
    return _to_art(out)


