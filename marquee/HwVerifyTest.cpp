// On-device ECDSA-P256 verify test fixture (jrwagz/marquee-scroller#100).
//
// jrwagz asked for proof that the new ECDSA-P256 verify path behaves exactly
// as expected on real ESP8266 hardware (the prior round swapped HMAC-SHA256
// → ECDSA-P256 in 09b76b2 after he correctly pointed out that a build-time
// HMAC key is trivially extractable from the public firmware bin).  This
// fixture compiles into the firmware only when -DWAGFAM_HW_VERIFY_TEST=1 is
// set, runs immediately in setup() before any networking, hits the SAME
// verifyConfigUpdateSignature() path the production poll uses, and prints
// one [ECDSA-TEST] line per case to Serial so the result is greppable.
//
// Coverage:
//   1. positive — valid sig + key + payload                        → expect TRUE
//   2. wrong-signature — sig was made for a DIFFERENT payload      → expect FALSE
//   3. wrong-public-key — sig is fine, key is from a 2nd keypair   → expect FALSE
//   4. truncated-payload — sign full, verify against payload[:-1]  → expect FALSE
//   5. mutated-payload — sign canonical, verify against 1-byte-flip → expect FALSE
//   6. mutated-signature — flip 1 byte in the 64-byte raw r||s     → expect FALSE
//   7. wrong-prefix-pubkey — replace 0x04 with 0x02 (compressed)   → expect FALSE
//   8. empty-pubkey — pass ""                                       → expect FALSE
//
// Cases 7 and 8 hit the early-reject paths in verifyConfigUpdateSignature
// (the 130-char hex length check and the 0x04-prefix check); cases 1-6
// exercise the BearSSL ECDSA path itself (br_ecdsa_i15_vrfy_raw with
// br_ec_p256_m15) and should round-trip bit-identically with what
// jrwagz/wagfam-server's signing path emits.
//
// Fixtures live in the marker block below and are regenerated by
// scripts/sign_test_payloads.py.

