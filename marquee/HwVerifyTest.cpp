namespace {

struct TestCase {
  const char *name;
  const char *payload;
  const char *signatureBase64;
  const char *publicKeyHex;
  bool expected;
};

void runOne(const TestCase &tc, uint8_t &passes) {
  // Construct the same argument shapes verifyConfigUpdateSignature sees in
  // production (marquee.ino's poll handler around line 1133).  String for
  // payload + sig (because both come from JSON deserialization there),
  // const char* for the pubkey (because production passes
  // WAGFAM_CONFIG_PUBLIC_KEY directly).
  String payload(tc.payload);
  String signatureBase64(tc.signatureBase64);
  bool got = verifyConfigUpdateSignature(payload, signatureBase64, tc.publicKeyHex);
  bool ok = (got == tc.expected);
  Serial.print(F("[ECDSA-TEST] "));
  Serial.print(tc.name);
  Serial.print(F(": "));
  Serial.print(ok ? F("PASS") : F("FAIL"));
  Serial.print(F(" (expected="));
  Serial.print(tc.expected ? F("true") : F("false"));
  Serial.print(F(", got="));
  Serial.print(got ? F("true") : F("false"));
  Serial.println(F(")"));
  if (ok) passes++;
  // Each verify is ~30ms; yield between cases so the soft watchdog stays
  // happy even if BearSSL ends up doing more work than expected.
  yield();
}

}  // namespace

void runHwVerifyTest() {
  // Give the host's serial terminal a moment to attach after reset.  Without
  // this the first few lines can be eaten when the user opens minicom/screen
  // a heartbeat after the device boots.
  delay(500);
  Serial.println();
  Serial.println(F("[ECDSA-TEST] === ECDSA-P256 verify hardware test (PR #100) ==="));
  Serial.println(F("[ECDSA-TEST] Fixtures from scripts/sign_test_payloads.py"));
  Serial.println(F("[ECDSA-TEST] Each case calls verifyConfigUpdateSignature() — same path as the production poll."));

  const TestCase kCases[] = {
      // 1. The positive case proves the BearSSL P-256 verify accepts a
      //    signature that was produced by the matching private key over the
      //    exact bytes we pass in.  If THIS one fails, the rest are noise.
      {"01_positive",                kPayloadCanonical, kSigCanonicalB64,  kPubKeyMainHex,     true},
      // 2. Same payload + same key, but the signature was produced over a
      //    *different* payload.  Sig is well-formed (still 64 bytes raw),
      //    but the digest mismatch should make ECDSA reject.
      {"02_wrong_signature",         kPayloadCanonical, kSigForOtherB64,   kPubKeyMainHex,     false},
      // 3. Sig + payload are consistent, but we verify with a public key
      //    from a different keypair.  Tests that the verify is actually
      //    using the supplied key, not some cached state.
      {"03_wrong_public_key",        kPayloadCanonical, kSigCanonicalB64,  kPubKeyAltHex,      false},
      // 4. Truncated payload — drops the trailing '}'.  Same key+sig, but
      //    SHA-256 of the shorter byte string mismatches.
      {"04_truncated_payload",       kPayloadTruncated, kSigCanonicalB64,  kPubKeyMainHex,     false},
      // 5. Mutated payload — one ASCII byte flipped ('h' → 'H').  Catches a
      //    class of bug where the implementation hashes a constant or
      //    capitalisation-folded version instead of the bytes-as-received.
      {"05_mutated_payload",         kPayloadMutated,   kSigCanonicalB64,  kPubKeyMainHex,     false},
      // 6. Mutated signature — one byte flipped in the raw 64-byte r||s
      //    *before* base64.  Still decodes to 64 bytes, just isn't a valid
      //    sig anymore.  Tests that we're not just length-checking.
      {"06_mutated_signature",       kPayloadCanonical, kSigMutatedB64,    kPubKeyMainHex,     false},
      // 7. Pubkey hex starts with 0x02 instead of 0x04.  Hits the early-
      //    reject branch in verifyConfigUpdateSignature — should fail
      //    BEFORE BearSSL is even called.  We mandate uncompressed form
      //    because we control both ends of the pipe.
      {"07_wrong_prefix_pubkey",     kPayloadCanonical, kSigCanonicalB64,  kPubKeyWrongPrefix, false},
      // 8. Empty pubkey — the firmware default when WAGFAM_CONFIG_PUBLIC_KEY
      //    isn't passed at build time.  Hits the very first check in
      //    verifyConfigUpdateSignature; this is the "fail closed" path.
      {"08_empty_pubkey",            kPayloadCanonical, kSigCanonicalB64,  "",                 false},
  };

  uint8_t passes = 0;
  const uint8_t total = sizeof(kCases) / sizeof(kCases[0]);
  unsigned long startMs = millis();
  for (uint8_t i = 0; i < total; i++) {
    runOne(kCases[i], passes);
  }
  unsigned long elapsedMs = millis() - startMs;

  Serial.print(F("[ECDSA-TEST] DONE: "));
  Serial.print(passes);
  Serial.print(F("/"));
  Serial.print(total);
  Serial.print(F(" passed in "));
  Serial.print(elapsedMs);
  Serial.println(F(" ms"));
  Serial.println(F("[ECDSA-TEST] === halting; reset the device to re-run ==="));

  // Halt — feed the watchdog so the device stays alive and the user has
  // time to scroll up in the serial monitor.  Re-print the summary every
  // 30s so it's easy to find no matter when serial is attached.
  unsigned long lastEchoMs = millis();
  while (true) {
    yield();
    delay(100);
    if (millis() - lastEchoMs > 30000UL) {
      Serial.print(F("[ECDSA-TEST] (still halted) DONE: "));
      Serial.print(passes);
      Serial.print(F("/"));
      Serial.print(total);
      Serial.println(F(" passed"));
      lastEchoMs = millis();
    }
  }
}

#endif  // WAGFAM_HW_VERIFY_TEST
